import {
  DATA_SUCCESS,
  DATA_FAIL,
  SET_MESSAGE,
} from "./types";

import adminClientService from "../services/adminClient.service";

export const ClientAdd = (name, contact_person, mobile_no, email_id, address, location, pincode,warranty,site_name,site_location) => (dispatch) => {
  return adminClientService.ClientAdd(name, contact_person, mobile_no, email_id, address, location, pincode,warranty,site_name,site_location).then(
    (response) => {
      dispatch({
        type: DATA_SUCCESS,
      });

      dispatch({
        type: SET_MESSAGE,
        payload: response.data.message,
      });

      return Promise.resolve(response);
    },
    (error) => {
      const message =
        (error.response &&
          error.response.data &&
          error.response.data.message) ||
        error.message ||
        error.toString();

      dispatch({
        type: DATA_FAIL,
      });

      dispatch({
        type: SET_MESSAGE,
        payload: message,
      });

      return Promise.reject();
    }
  );
};

